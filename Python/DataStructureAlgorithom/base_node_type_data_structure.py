class ListNode(object):
    def __init__(self, value=0, next=None):
        self.value = value
        self.next = next

class TreeNode(object):
    def __init__(self, node=None):
        self.name = "树形数据结构"
        self.right = None
        self.left = None
        self.node = node


class GraphNode(object):
    def __init__(self):
        self.name = "图数据结构"
